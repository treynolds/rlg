    private void updateTable(){
        dtm=(DefaultTableModel)jTable1.getModel();
        for(int a=0;a<dtm.getRowCount();a++){
            Object val0=dtm.getValueAt(a, 0);
            Object val1=dtm.getValueAt(a, 1);
            String vs0= (val0==null)? "":val0.toString();
            String vs1= (val1==null)? "":val1.toString();
            if(!("").equals(vs0)){
                String s=(String)dtm.getValueAt(a, 0);
                String[] sa=s.split("u");
                String v = "";
                for(int b=1;b<sa.length;b++){
                    char ch = (char)Integer.parseInt(sa[b],16);
                    v = v + ch;
                }
                dtm.setValueAt(v, a, 1);
            } else if(!("").equals(vs1)){
                String s=(String)dtm.getValueAt(a, 1);
                String v="";
                for(int b=0;b<s.length();b++){
                    String u = "0000" + Integer.toHexString(s.charAt(b));
                    v += "u" + u.substring(u.length()-4);
                }
                dtm.setValueAt(v, a, 0);
            }
        }
    }

