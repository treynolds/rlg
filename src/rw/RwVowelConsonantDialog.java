    private void downPageButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_downPageButtonActionPerformed
        String cv=currentCharField.getText();
        cv=cv.substring(1);
        int val = Integer.parseInt(cv,16);
        val -= 256;
        if(val <= 0){
            val = 65535 - val;
        }
        cv = Integer.toHexString(val);
        cv = ("0000" +  cv).substring(cv.length());
        jTable1.setValueAt("u" + cv, jTable1.getSelectedRow(), 0);
        currentCharField.setText("u" + cv);
        updateButtonActionPerformed(evt);
    }//GEN-LAST:event_downPageButtonActionPerformed

